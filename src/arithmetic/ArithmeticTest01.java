package arithmetic;

public class ArithmeticTest01 {

	public static void main(String[] args) {
		// 산술연산 : + - * /  % 

		int a = 10;
		int b = 20;

		System.out.println(a + b); // Output : 30
		System.out.println(a - b); // Output : -10
		System.out.println(a * b); // Output : 200
		System.out.println(a / b); // Output : 0 정수/정수 = 정수 : 나누어서 몫의 값 (0.5지만 0만)
		System.out.println(a % b); // Output : 10 : 나누어서 나머지의 값
		
		
	}

}
