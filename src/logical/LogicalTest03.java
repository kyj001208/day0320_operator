package logical;

public class LogicalTest03 {

	public static void main(String[] args) {
		// 논리연산자 : && (그리고), || 또는, !(부정)

		int a=10;
		int b=20;	
		
		boolean result = a>b;
		System.out.println(result);  //OutPut : false
		System.out.println(!result); //OutPut : true (답에 대해 부정하라는 의미)
		
		
		
	}

}
